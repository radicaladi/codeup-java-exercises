public class StringExercise {
    public static void main(String[] args) {

        String sentence = "We don't need no education\n";
        sentence = sentence + "We don't need no thought control";
        System.out.println(sentence);

        sentence = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(sentence);

        sentence = "In windows, the main drive is usually C:\\";
        System.out.println(sentence);

        sentence = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(sentence);

    }
}
